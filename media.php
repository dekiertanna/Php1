                                    <hr>
                                    Mówi się, że powstanie samochodów Lamborghini wynikło z dyskusji pomiędzy miłośnikiem wozów sportowych Ferruccim Lamborghinim (1916-1993) a Enzo Ferrarim. Lamborghini, który wówczas z powodzeniem produkował wyłącznie traktory, był niezadowolony ze swego samochodu Ferrari i zaproponował Enzo Ferrariemu wprowadzenie zmian konstrukcyjnych. Ten oburzony uwagami "jakiegoś traktorzysty" zakwestionował kompetencje Lamborghiniego. Wobec tego Lamborghini postanowił udowodnić mu kto jest lepszy i już po roku zademonstrował swój legendarny 12-cylindrowy model Lamborghini 350 GTV, wyraźnie przewyższający samochody marki Ferrari.

W 1966 pojawił się model Miura, dzięki któremu firma zyskała sławę na całym świecie. Mimo że samochód ten jest narowisty i trudny w prowadzeniu, to jego miłośnikom w niczym to nie przeszkadza.

W 1971 roku na salonie samochodowym w Genewie pokazano prototyp Countach, zaprojektowany przez Marcello Gandiniego z firmy Bertone. W 1974 roku rozpoczęła się seryjna produkcja. Ostro cięte, niskie nadwozie z unoszonymi do góry drzwiami i wlotami powietrza nadało mu niepowtarzalny charakter. Model ten, produkowany 15 lat pozwolił firmie i jej kolejnym właścicielom przetrwać ciężkie czasy.

Ferruccio Lamborghini sprzedał dział zajmujący się produkcją aut sportowych już w 1972 roku. Kolejnymi właścicielami byli Georges-Henri Rossetti i René Leimer, następnie zarządzający firmą Patrick Mimran, który w 1987 roku sprzedał udziały firmie Chrysler, która sprzedała ją w 1994 roku Indonezyjskiej firmie Megatech. W 1998 roku Automobili Lamborghini S.p.A po raz ostatni, jak do tej pory, zmieniło właściciela, którym zostało Audi AG.

W 1982 roku Lamborghini przedstawiło prototyp surowego samochodu terenowego oznaczony jako LM001. W 1986 roku, po dokonaniu szeregu modyfikacji, wszedł do produkcji seryjnej jako LM002. Dzięki swojemu wyglądowi do dziś pozostaje jednym z najbardziej oryginalnych samochodów terenowych wszech czasów. Następcą modelu Countach w 1990 roku zostało Diablo i produkowany był przez ponad dziesięć lat. Z kolei jego następca, model Murciélago, pojawił się dopiero w 2001 roku. Małe modele Lamborghini były produkowane z dużymi przerwami. Po modelu Urraco trzeba było czekać trzy lata na kolejny "mały" model, Jalpa, który produkowano do 1989 roku. W 1995 przedstawiono prototyp Cala z silnikiem V10 3.9 L . o mocy 400 KM. Do seryjnej produkcji jednak nie doszło. Dopiero model Gallardo w 2003 roku godnie zastąpił poprzednie modele. Ciekawostką jest to, że marka Lamborghini oznacza oprócz supersamochodów przede wszystkim ciągniki rolnicze i po Fiacie jest drugim co do wielkości ich producentem we Włoszech.
